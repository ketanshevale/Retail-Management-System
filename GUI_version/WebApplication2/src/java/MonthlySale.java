public class MonthlySale {
    private String PID;
    private String PNAme;
    private int QTY;
    private String PYEAR;
    private String PMONTH;
    private double TOTAL_PRICE;
    private double AverageSale;

    /**
     * @return the PID
     */
    public String getPID() {
        return PID;
    }

    /**
     * @param PID the PID to set
     */
    public void setPID(String PID) {
        this.PID = PID;
    }

    /**
     * @return the PNAme
     */
    public String getPNAme() {
        return PNAme;
    }

    /**
     * @param PNAme the PNAme to set
     */
    public void setPNAme(String PNAme) {
        this.PNAme = PNAme;
    }

    /**
     * @return the QTY
     */
    public int getQTY() {
        return QTY;
    }

    /**
     * @param QTY the QTY to set
     */
    public void setQTY(int QTY) {
        this.QTY = QTY;
    }

    /**
     * @return the PYEAR
     */
    public String getPYEAR() {
        return PYEAR;
    }

    /**
     * @param PYEAR the PYEAR to set
     */
    public void setPYEAR(String PYEAR) {
        this.PYEAR = PYEAR;
    }

    /**
     * @return the PMONTH
     */
    public String getPMONTH() {
        return PMONTH;
    }

    /**
     * @param PMONTH the PMONTH to set
     */
    public void setPMONTH(String PMONTH) {
        this.PMONTH = PMONTH;
    }

    /**
     * @return the TOTAL_PRICE
     */
    public double getTOTAL_PRICE() {
        return TOTAL_PRICE;
    }

    /**
     * @param TOTAL_PRICE the TOTAL_PRICE to set
     */
    public void setTOTAL_PRICE(double TOTAL_PRICE) {
        this.TOTAL_PRICE = TOTAL_PRICE;
    }

    /**
     * @return the AverageSale
     */
    public double getAverageSale() {
        return AverageSale;
    }

    /**
     * @param AverageSale the AverageSale to set
     */
    public void setAverageSale(double AverageSale) {
        this.AverageSale = AverageSale;
    }
    
    
}
